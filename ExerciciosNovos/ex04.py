'''
    Crie um algoritmo que leia um número e mostre o seu dobro, o seu triplo e sua raiz quadrada:
'''
num = int(input("Diga um numero para transformacao: "))

dobro = num*2
triplo = num*3
raiz = num**(1/2)

print(f"seu dobro é {dobro}")
print(f"seu triplo é {triplo}")
print(f"sua raiz é {raiz:.1f}")




